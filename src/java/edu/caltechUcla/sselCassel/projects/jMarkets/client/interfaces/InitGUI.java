    private void SubmitButtonAction(java.awt.event.ActionEvent evt) {
        try{
            name = NameText.getText();
            host = HostText.getText();
            if (name == null || name.length() < 1)
                JOptionPane.showMessageDialog(null, "You must enter a valid name to proceed", "Input Error", JOptionPane.ERROR_MESSAGE);
            else if (host == null || host.length() <1)
                JOptionPane.showMessageDialog(null, "You must enter a valid host to proceed", "Input Error", JOptionPane.ERROR_MESSAGE);
            else {
                setVisible(false);
            }
        }catch(Exception e) {
            //log.error("Failed to get the client information");
        }
    }
    

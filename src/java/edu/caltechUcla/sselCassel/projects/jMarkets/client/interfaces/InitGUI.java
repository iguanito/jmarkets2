    /** Enable or disable the host and name fields depending on if they were
     *  automatically generated or not. If both were automatically generated,
     *  then automatically submit the client */
    public void fillFields() {
        if (host != null) {
            HostText.setText(host);
            HostText.setEnabled(false);    
        }
        if (name != null) {
            NameText.setText(name);
            NameText.setEnabled(false);
        }
        
        this.setVisible(true);
        
        if (!HostText.isEnabled() && !NameText.isEnabled())
            autoSubmit();
    }
    
    /** Wait a few seconds then submit automatically. Don't wait a few seconds if
     *  this is an integrated test */
    private synchronized void autoSubmit() {
        try{      
            Runnable doUpdate = new Runnable() {
                public void run() {
                    SubmitButtonAction(null);
                }
            };
            SwingUtilities.invokeLater(doUpdate);
        }catch(Exception e) {
        }
    }
    
    /* Center on the screen */
    public void centerOnScreen() {
        Dimension screen = Toolkit.getDefaultToolkit().getScreenSize();
        Dimension window = this.getSize();
        int iCenterX = screen.width / 2;
        int iCenterY = screen.height / 2;
        setLocation(iCenterX - (window.width / 2),
        iCenterY - (window.height / 2));
    }
    
